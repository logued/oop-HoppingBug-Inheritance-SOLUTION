package enums;

/**
 * Enumeration class defines a TYPE called Direction.
 * The purpose of the type is to allow us to declare a variable that
 * can have only the values listed in the braces. {LEFT,RIGHT}
 * We can use this to declare a named variable of type Direction.
 * e.g. Direction myDirection = Direction.LEFT;
 * The variable myDirection can be assigned only the two values
 * "Direction.LEFT" and "Direction.RIGHT"
 *
 */
public enum Direction{ LEFT, RIGHT };
